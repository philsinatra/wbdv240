						<u>Grading criteria:</u>
					</p>
					<dl>
						<dt> Code Quality </dt>
						<dd>
							<p>
								There are best practices listed under all of the lecture notes sections. Be sure to follow the rules and apply our techniques from class.
							</p>
							<ul>
								<li>validate your code, (<a href="http://tv.adobe.com/watch/cs-55-web-premium-feature-tour-/dreamweaver-cs-55-w3c-validation" target="_blank">with Dreamweaver</a>) there should be no errors (<a href="http://validator.w3.org/" target="pwin">validator.w3.org</a>)</li>
								<li>follow the best practices listed on the <a href="lecture.php">lecture notes page</a> regarding file names, characters and coding styles</li>
								<li>your code is legible and easy to navigate</li>
								<li>your code should use comments wherever possible</li>
							</ul>
						</dd>
						<dt> Functionality </dt>
						<dd>
							<p>
								Everything needs to work. All links, videos, audio, images, slideshows, rollovers etc. If it's part of your site, it should be working properly. Your resume <b>MUST</b> be programmed in HTML. You can not simply embed a pdf into your webpage.
							</p>
						</dd>
						<dt> Design </dt>
						<dd>
							<p>
								Compare your midterm and your final website.
							</p>
							<ul>
								<li>Does everything look the same? </li>
								<li>Has the design changed? </li>
								<li>If it did, why? </li>
							</ul>
							<p>
								Enhancements are great. Changing something because you didn't get around to implementing it or couldn't figure it out are not valid excuses. If it was in your midterm it should be in your final, unless you have made it better or discussed in class with the instructor why something is changing prior to week 10.
							</p>
						</dd>
					</dl>
